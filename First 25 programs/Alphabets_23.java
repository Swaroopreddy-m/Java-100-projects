
// The Program is used to print a to z alphabates using for loop.



public class Alphabets_23 {
    public static void main(String[] args) {
        char c;

        System.out.println("Alphabates using char variable and for loop :");
        for(c = 'a'; c <= 'z'; c++){
            System.out.println(c);

        }
    

        
    }
}
