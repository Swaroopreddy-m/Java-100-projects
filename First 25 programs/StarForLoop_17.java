public class StarForLoop_17 {
    public static void main(String[] args) {           // program uesd to print 10 stars in a row and 10 coluns.
        int row,stars;
        for(row=1;row<=10;row++){
            for(stars=1;stars<=10;stars++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
